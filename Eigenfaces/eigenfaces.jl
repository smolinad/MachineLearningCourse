md"## Eigenfaces Algoritm"

# ╔═╡ 46abdcde-ff03-4151-9b2f-190d6409cc13
md"Let $X$ be the $m \times n$ matrix representing $n$ face images with $m$ pixels —with $m = \operatorname{height} \times \operatorname{width}$—, substracting the mean of the face images —columns— to each column in $X$. The previous substraction is done to normalize the face images. To create the eigenfaces, we apply the following algorithm:
1. Calculate the covariance matrix of $X$, *i.e.* $\operatorname{cov}(X) = \frac{XX^T}{n}$. 
2. Calculate the eigenvalues and eigenvectors of $\operatorname{cov}(X)$.
3. Choose the principal components, *i.e.* sort the eigenvalues of $\operatorname{cov}(X)$ and its related eigenvectors in descending order.
4. Reconstruct a face $x$ outside the dataset $X$, by calculating $\tilde{U_k}\tilde{U_k}^Tx$, where $\tilde{U_k}$ is the matrix containing the first $k$ columns of the eigenvectors matrix obtained in the previous step, corresponding to the first $k$ principal components.

Now, consider the Singular Value Decomposition of $X$,

$$X = U \Sigma V^T.$$

Therefore, the decomposition of $XX^T$ is

$$\begin{align*}
	XX^T &= U \Sigma V^T (U \Sigma V^T)^T \\
	     &= U \Sigma V^T V \Sigma^T  U^T\\
 		 &= U \Sigma \Sigma^T  U^T,& \text{as $V$ is unitary, $V^T V = I$.}\\
		 &= U \Lambda U^T,
\end{align*}$$

where $\Lambda = diag(\lambda_i)$ is the diagonal matrix of eigenvalues of $XX^T$. It follows that $\lambda_i = \frac{(\sigma_i)^2}{n}$, where $\sigma_i$ are the singular values of the SVD of $X$, and the **eigenfaces** are the column vectors of $U$, related to the positive singular values. In this way, the Eigenfaces Algortihm is directly related to SVD. With SVD, is not necessary to calculate the covariance matrix of $X$. We implement the algorithm here:
"

# ╔═╡ eebbd10a-e869-4b6f-936f-0f39b8e59230
function eigenfaces(train)
	X = train .- mean(train, dims=2)
	U, S, VT = svd(X)

	U, S, VT
end

# ╔═╡ b7c533d9-9457-4b66-ad3e-9c2c6d974eba
U, S, VT = eigenfaces(training_faces);

# ╔═╡ db72f22f-0c24-46ee-9cb2-888ad264c5b3
md"## Cumulative Variance Analysis"

# ╔═╡ 5b48507a-55dd-464f-9477-dcf6ece947a5
md"Reconstruct a face image not included in $X$. The minimum number $k$ of principal components that can be used to represent $\varepsilon$ of the total variance of the face images can be calculated as 

$k = \min_k \left\{ \frac{\sum_{i=1}^k\lambda_i}{n} \geq \varepsilon \right\},$

where $\lambda_i$ are the eigenvalues of $\operatorname{cov}(X)$."

# ╔═╡ 357ae011-0942-4a48-9cc4-15122b097158
begin
	explained_var = S / sum(S)
	cumulative_var = cumsum(explained_var)
	n_components = findfirst(cumulative_var .>= 0.95)
end

# ╔═╡ 86b07c5d-301f-4789-bbf2-a044c8868874
md"## Testing"

# ╔═╡ 4cd72a2f-4f00-4b6d-b351-9ba5c6e6e3be
md"As in the Eigenfaces Algorithm explanation, a face $x$ outside the training set can be reconstructed calculating $\widetilde{U_k}\widetilde{U_k}^Tx$. This is clearly an approximation, as we are not using the whole matrix $U$. It follows from 
[Eckart-Young Theorem](https://en.wikipedia.org/wiki/Low-rank_approximation) for low-rank approximation, that in fact, 

$\operatorname{argmin}_{\tilde{X} : \operatorname{rank}(\tilde{X}) \leq k} \lVert
X - \tilde{X} \rVert_F = \widetilde{U_k}\widetilde{\Sigma_k}\widetilde{V_K}^T.$

Therefore, by calculating $\widetilde{U_k}^Tx$ we obtain the projection of the new face image $x$ into the orthonormal subspace of eigenfaces, *i.e.* the weights of the principal components that make up $x$. By calculating  $\widetilde{U_k}\widetilde{U_k}^Tx$, we obtain the corresponding linear combination of the approximation.
"

# ╔═╡ 93e09a88-5207-4691-8562-d203c0a6b92f
function reconstructFace(train, test, r)
	test_face_minus_avg = test - mean(train, dims=2)
	weights = transpose(U[:,1:r]) * test_face_minus_avg
	reconst = U[:,1:r] * weights

	## Gives the indices of the top-10 most important eigenfaces
	ranked = sortperm(vec(weights), rev=true)[1:10]
	
	reconst, ranked, weights
end

